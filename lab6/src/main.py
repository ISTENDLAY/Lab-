# Найдите среди целых чисел, принадлежащих числовому отрезку [174457; 174505], числа, имеющие ровно два различных 
# натуральных делителя, не считая единицы и самого числа. Для каждого найденного числа запишите эти два делителя в два 
# соседних столбца на экране с новой строки в порядке возрастания произведения этих двух делителей. Делители в строке также 
# должны следовать в порядке возрастания. 
print('Task 3:')
def f(x):
    n=0
    d = []
    for i in range(2, int(x**0.5)+2):
        if x%i==0:
            n+=2
            d.append(i)
            d.append(x//i)
    if n==2:
        return d
    else:
        return 0
for i in range(174457, 174505+1):
    r = f(i)
    if r!=0:
        print(r)
